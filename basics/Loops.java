public class Loops {
    public static void main(String[] args) {
        // For loop
        for (int i = 1; i <= 5; i++) {
            System.out.println("Count: " + i);
        }

        // While loop
        int count = 0;
        while (count < 5) {
            System.out.println("While count: " + count);
            count++;
        }
    }
}
